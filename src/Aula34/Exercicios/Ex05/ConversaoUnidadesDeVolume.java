package Aula34.Exercicios.Ex05;

class ConversaoUnidadesDeVolume {

    public static double litroParaCmCubicos(double num1){
        return num1*1000;
    }
    public static double metroCubicoParaLitros(double num1){
        return num1*1000;
    }
    public static double metroCubicoParaPesCubicos(double num1){
        return num1*35.32;
    }
    public static double galaoAmericanoParaPolegadasCubicas(double num1){
        return num1*231;
    }
    public static double galaoAmericanoParaLitros(double num1){
        return num1*3.785;
    }
}
