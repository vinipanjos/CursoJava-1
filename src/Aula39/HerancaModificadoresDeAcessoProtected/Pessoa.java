package Aula39.HerancaModificadoresDeAcessoProtected;

public class Pessoa {

    String nome; //default somente a classe e o pacote podem ver
    private String endereco;
    private String telefone;
    private String cpf;
    private String telefoneCelular; //private somente a classe pode ver

    protected String NomeVisibilidade; //protected class, pacote e subclassle podem ver
    //todos projeto pode ver
    public Pessoa(){}

    public Pessoa(String nome, String endereco, String telefone) {
        super();
        this.nome = nome;
        this.endereco = endereco;
        this.telefone = telefone;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getTelefoneCelular() {
        return telefoneCelular;
    }

    public void setTelefoneCelular(String telefoneCelular) {
        this.telefoneCelular = telefoneCelular;
    }
}
