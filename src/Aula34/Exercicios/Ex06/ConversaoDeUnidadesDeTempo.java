package Aula34.Exercicios.Ex06;

class ConversaoDeUnidadesDeTempo {

    public static double minutoParaSegundo(double num1){
        return num1*60;
    }
    public static double horaParaMinuto(double num1){
        return num1*60;
    }
    public static double diaParaHora(double num1){
        return num1*24;
    }
    public static double semanaParaDia(double num1){
        return num1*7;
    }
    public static double anoParaDia(double num1){
        return num1*365.25;
    }
    public static double mesParaDia(double num1){
        return num1*30;
    }
}
