package Aula34.Exercicios.Ex04;

class ConversaoDeUnidadesDeArea {

    public static double metroQuadrado(double num1){
        return num1*1000;
    }
    public static double peQuadrado(double num1){
        return num1*929;
    }
    public static double milhaQuadrada(double num1){
        return num1*640;
    }
    public static double acre(double num1){
        return num1*43560;
    }



}
